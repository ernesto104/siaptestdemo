    public void actualizaMontoARenovar(){
        Double adelanto;
        Double gastosAdministrativos;
        
        if(!txtAdelanto.getText().trim().equals("")&&!txtGastosAdministrativos.getText().trim().equals("")){
           try {
            adelanto = Double.parseDouble(txtAdelanto.getText());
            gastosAdministrativos = Double.parseDouble(txtGastosAdministrativos.getText());
            DecimalFormat formato = new DecimalFormat("#.00");
            txtMontoARenovar.setText(formato.format(Double.parseDouble(txtMontoLetra.getText())- adelanto + gastosAdministrativos).replace(",", "."));
            
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese Numeros válidos", "Error Número Ingresado", JOptionPane.WARNING_MESSAGE);
            }
        
        }
            
    
    }
