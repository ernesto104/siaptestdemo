    private void btnGenerarRenovacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarRenovacionActionPerformed
        //validar
        serv_doc = new Servicio_Documentos();
        int numeroRenovacion;
        if(txtMontoARenovar.getText().equals("")||dt_fechaVencimiento.getDate() == null|| txtRenovacion.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Error: Verificar Fecha de vencimiento, Monto a Renovar o Nro. Renovación", "Datos imcompletos", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                numeroRenovacion = Integer.parseInt(txtRenovacion.getText());

            sisLet = serv_doc.getSis("Número de Letra");
            int opcion = JOptionPane.showConfirmDialog(null, "¿Esta seguro de Generar la Letra Renovada ?", "Confirmacion", JOptionPane.YES_NO_OPTION);
            Date dateFechaEmision = null;
            if (opcion == JOptionPane.YES_OPTION) {
                String fechaEmision = txtFechaEmision.getText();
                SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
            try {
                dateFechaEmision = formatter.parse(fechaEmision);
            } catch (ParseException ex) {
                System.out.println("Error en formato de fecha "+ex.getMessage());
            }
                
            int diferenciaDias = (int) ((dateFechaEmision.getTime()-dt_fechaVencimiento.getDate().getTime())/86400000);            
                System.out.println("Diferencia de Dias : "+ diferenciaDias);
                NroLetras nroLetra = new NroLetras();
                nroLetra.setNro(1);
                nroLetra.setDiasVcto(diferenciaDias);//temporal calcularlo
                nroLetra.setImporte(Double.parseDouble(txtMontoARenovar.getText()));
                nroLetra.setFechaVec(dt_fechaVencimiento.getDate());
                nroLetra.setNroRenovacion(numeroRenovacion);
                String tipoLetra = String.valueOf(cbTipoLetra.getSelectedItem()).substring(0, 2);
                System.out.println("tipoLetra:" + tipoLetra);
               
             
            ArrayList<Cabeces> cabec_letras = SetCabeces(nroLetra, tipoLetra);
            if(serv_doc.GuardarLetras_Varias(cabec_letras)){
                JOptionPane.showMessageDialog(null, "Guardado con Exito", "Guardar", JOptionPane.INFORMATION_MESSAGE);
                serv_doc.actualizarSis(sisLet);
                frep43.limpiar();
                frep43.model.getDataVector().removeAllElements();
                frep43.model.fireTableDataChanged();
                frep43.iniciarTabla(tablaLetras);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Error al Guardar", "Guardar", JOptionPane.ERROR_MESSAGE);
            }

        }
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: Nro. Renovación debe ser un numero entero", "Numero", JOptionPane.ERROR_MESSAGE);
        }
     }

        
    }//GEN-LAST:event_btnGenerarRenovacionActionPerformed

