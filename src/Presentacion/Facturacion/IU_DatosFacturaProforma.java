    private void txtNroGuiaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNroGuiaKeyTyped
        
        if ( txtNroGuia.getText().length() == 30 ) {
            evt.consume();
        }
        
        char caracter = evt.getKeyChar();
        if ( caracter >= 'a' && caracter <= 'z' ) {
            evt.setKeyChar( (char) (caracter - 32) );
            
        } else if ( caracter == 'ñ') {
            char nuevo = 'Ñ';
            evt.setKeyChar(nuevo);
            
        } else if ( caracter >= 'A' && caracter <= 'Z' || caracter == ' ' || caracter == 'Ñ' || caracter == '.' || caracter == '-') {
            evt.setKeyChar(caracter);
            
        } else if ( caracter >= '0' && caracter <= '9' ) {
            evt.setKeyChar(caracter);
            
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_txtNroGuiaKeyTyped

