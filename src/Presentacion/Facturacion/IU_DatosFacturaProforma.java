    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        int confirmacion = tipoMensaje.Confirmacion("¿ Está seguro de actualizar los datos ?");
        
        if ( confirmacion == tipoMensaje.SI ) {
            String numeroFactura = txtNumFactura.getText();
            cabecProforma.setNrofactura(numeroFactura);
            cabecProforma.setFechafact(jdcFechaFactura.getDate());
            cabecProforma.setTransportista(txtTransportista.getText());
            cabecProforma.setNroguia(txtNroGuia.getText());
            cabecProforma.setFechadespacho(jdcFechaDespacho.getDate());
            
            servicioDocumento = new Servicio_Documentos();
            
            if ( servicioDocumento.actualizarCabecProforma(cabecProforma) ) {
                tipoMensaje.Mensaje(tipoMensaje.EXITO_MODIFICAR);
                tablaProforma.setValueAt(numeroFactura , filaSeleccionada, 3);
                SimpleDateFormat formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
                String fechaFormateada = formatoFecha.format(jdcFechaFactura.getDate());
                tablaProforma.setValueAt(fechaFormateada, filaSeleccionada, 4);
                
            } else {
                tipoMensaje.Mensaje(tipoMensaje.ERROR_MODIFICAR);
            }
        }
        dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtNumFacturaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumFacturaKeyTyped
        
        if ( txtNumFactura.getText().length() == 12 ) {
            evt.consume();
        }
        
        char caracter = evt.getKeyChar();
        
        if ( !(caracter >= '0' && caracter <= '9') && caracter != '-') {
            evt.consume();
        }
    }//GEN-LAST:event_txtNumFacturaKeyTyped

    private void txtTransportistaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTransportistaKeyTyped
        
        if ( txtTransportista.getText().length() == 50 ) {
            evt.consume();
        }
        
        char caracter = evt.getKeyChar();
        if ( caracter >= 'a' && caracter <= 'z' ) {
            evt.setKeyChar( (char) (caracter - 32) );
            
        } else if ( caracter == 'ñ') {
            char nuevo = 'Ñ';
            evt.setKeyChar(nuevo);
            
        } else if ( caracter >= 'A' && caracter <= 'Z' || caracter == ' ' || caracter == 'Ñ' ) {
            evt.setKeyChar(caracter);
            
        } else {
            evt.consume();
        }
    }//GEN-LAST:event_txtTransportistaKeyTyped

