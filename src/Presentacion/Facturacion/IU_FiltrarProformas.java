    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed

        if ( rb_cliente.isSelected() && !chFecha.isSelected()) { // FILTRO SÃ³lo CLIENTE
            String clienteSeleccionado = String.valueOf(cb_cliente.getSelectedItem());
            
            if ( "TODOS".equals(clienteSeleccionado) ){
                iuProforma.Listar(sf.filtrarCabecproformas(0));
                
            } else {
                Clientes clienteElegido = new ClienteDAO().obtenerClientePorNombre(clienteSeleccionado);
                iuProforma.Listar(sf.filtrarCabecproformas(clienteElegido.getIdcliente()));
            }
            
        } else if ( rb_estado.isSelected() && !chFecha.isSelected() ) { // FILTRO SÃ³lo ESTADO
            String estadoSeleccionado = String.valueOf(cb_estado.getSelectedItem());
            iuProforma.Listar(sf.filtrarCabecproformas(estadoSeleccionado));
            
        } else if ( !rb_cliente.isSelected() && !rb_estado.isSelected() && chFecha.isSelected()) { // FILTRO SÃ³lo FECHA
            iuProforma.Listar(sf.filtrarCabecproformas(jdc_desde.getDate(), jdc_hasta.getDate()));
            
        } else if ( rb_cliente.isSelected() && chFecha.isSelected() ) { // FILTRO CLIENTE Y FECHA (DOBLE)
            String clienteSeleccionado = String.valueOf(cb_cliente.getSelectedItem());
            Clientes clienteElegido = new ClienteDAO().obtenerClientePorNombre(clienteSeleccionado);
            iuProforma.Listar(sf.filtrarCabecproformas(clienteElegido.getIdcliente(), jdc_desde.getDate(), jdc_hasta.getDate()));
            
        } else if ( rb_estado.isSelected() && chFecha.isSelected() ) { // FILTRO ESTADO Y FECHA (DOBLE)
            String estadoSeleccionado = String.valueOf(cb_estado.getSelectedItem());
            iuProforma.Listar(sf.filtrarCabecproformas(estadoSeleccionado, jdc_desde.getDate(), jdc_hasta.getDate()));
        }
        dispose();
    }//GEN-LAST:event_btnFiltrarActionPerformed

    private void listarClientes() {
        cb_cliente.removeAllItems();
        Iterator<Clientes> it = sf.getListCli().iterator();
        cb_cliente.addItem("TODOS");
        while (it.hasNext()) {
            cb_cliente.addItem(it.next());
        }
    }
    
    private void rb_clienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rb_clienteMouseClicked
        if ( rb_cliente.isSelected() ) {
            listarClientes();
            cambiarEstadoFiltros(1);
        }
    }//GEN-LAST:event_rb_clienteMouseClicked

    private void rb_estadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rb_estadoMouseClicked
        if ( rb_estado.isSelected() ) {
            listarEstadosProforma();
            cambiarEstadoFiltros(2);
        }
    }//GEN-LAST:event_rb_estadoMouseClicked

    private void chFechaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_chFechaMouseClicked
        cambiarEstadoFiltros(3);
    }//GEN-LAST:event_chFechaMouseClicked

    private void listarEstadosProforma() {
        cb_estado.removeAllItems();
        String[] estados = {"TODOS", "PENDIENTE", "RECHAZADO"};
        for ( int i = 0; i < estados.length; i++ ){
            cb_estado.addItem(estados[i]);
        }
    }
    
    private void cambiarEstadoFiltros(int opcion) {
        
        switch (opcion){
            case 1: cb_cliente.setEnabled(true);
                    cb_estado.setEnabled(false);
                    cb_estado.removeAllItems();
                    break;
                
            case 2: cb_cliente.removeAllItems();
                    cb_cliente.setEnabled(false);
                    cb_estado.setEnabled(true);
                    break;
                
            case 3: cb_cliente.setEnabled(true);
                    cb_estado.setEnabled(true);
                    
                    if ( chFecha.isSelected() ) {
                        jdc_desde.setEnabled(true);
                        jdc_hasta.setEnabled(true);
                        
                    } else {
                        jdc_desde.setDate(null);
                        jdc_hasta.setDate(null);
                        jdc_desde.setEnabled(false);
                        jdc_hasta.setEnabled(false);
                    }
                    break;
        }
        repaint();
    }
